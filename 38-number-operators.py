'''
+   addition
-   subtraction
*   multiplication
/   division
**  exponentiation
%   modulo
//  integer division

PEMDAS = Parentheses → Exponents → Multiplication → Division → Addition → Subtraction 
'''

## if any part of the expression is a float, the end result will be a float
1 + 1.0 # 2.0
20 + 0.777 # 20.777

## divisions always return a float
1/2 # 0.5
10 / 3 # 3.3333333

## integer division

10 // 3 # 3
6 // 7 # 0 integer division always floors the result

2 ** 3 # 8
4 ** 8 # 65536
49 ** 0.5 # 7.0

10 % 3 # 1
25 % 4 # 1
13 % 2 # 1
16 % 2 # 0