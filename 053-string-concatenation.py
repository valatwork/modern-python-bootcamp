# Concatenation is combining multiple strings together. In Python you can do this simply with the "+" operator.

str_one = "your"
str_two = "face"
str_three = str_one + " " + str_two  # your face

# You can also use the "+=" operator!

str_one = "ice"
str_one += " cream"
str_one  # ice cream

